static void ssd1306_WriteCommand(uint8_t cmd)
{
    uint8_t data[2];
    data[0] = 0x00;
    data[1] = cmd;
    HAL_I2C_Master_Transmit(ssd1306_i2c, SSD1306_I2C_ADDR, data, 2, 100);
}

void SSD1306_Init(I2C_HandleTypeDef *hi2c)
{
    ssd1306_i2c = hi2c;

    HAL_Delay(100);

    ssd1306_WriteCommand(0xAE);
    ssd1306_WriteCommand(0x20);
    ssd1306_WriteCommand(0x00);
    ssd1306_WriteCommand(0xB0);
    ssd1306_WriteCommand(0xC8);
    ssd1306_WriteCommand(0x00);
    ssd1306_WriteCommand(0x10);
    ssd1306_WriteCommand(0x40);
    ssd1306_WriteCommand(0x81);
    ssd1306_WriteCommand(0x7F);
    ssd1306_WriteCommand(0xA1);
    ssd1306_WriteCommand(0xA6);
    ssd1306_WriteCommand(0xA8);
    ssd1306_WriteCommand(0x3F);
    ssd1306_WriteCommand(0xA4);
    ssd1306_WriteCommand(0xD3);
    ssd1306_WriteCommand(0x00);
    ssd1306_WriteCommand(0xD5);
    ssd1306_WriteCommand(0x80);
    ssd1306_WriteCommand(0xD9);
    ssd1306_WriteCommand(0xF1);
    ssd1306_WriteCommand(0xDA);
    ssd1306_WriteCommand(0x12);
    ssd1306_WriteCommand(0xDB);
    ssd1306_WriteCommand(0x40);
    ssd1306_WriteCommand(0x8D);
    ssd1306_WriteCommand(0x14);
    ssd1306_WriteCommand(0xAF);

    SSD1306_Fill(0);
    SSD1306_Update();
}

void SSD1306_Fill(uint8_t color)
{
    memset(ssd1306_buffer, color ? 0xFF : 0x00, sizeof(ssd1306_buffer));
}

void SSD1306_Update(void)
{
    for (uint8_t page = 0; page < 8; page++)
    {
        ssd1306_WriteCommand(0xB0 + page);
        ssd1306_WriteCommand(0x00);
        ssd1306_WriteCommand(0x10);

        uint8_t data[1 + 128];
        data[0] = 0x40;
        memcpy(&data[1], &ssd1306_buffer[128 * page], 128);

        HAL_I2C_Master_Transmit(ssd1306_i2c, SSD1306_I2C_ADDR, data, 1 + 128, 100);
    }
}

void SSD1306_SetCursor(uint8_t x, uint8_t y)
{
    cursor_x = x;
    cursor_y = y;   // y should be multiple of 8 for this simple 5x7 font
}

void SSD1306_WriteChar(char c)
{
    if (c < 32 || c > 126) c = '?';

    const uint8_t *glyph = Font5x7[c - 32];
    uint16_t index = cursor_x + (cursor_y / 8) * 128;

    for (int i = 0; i < 5; i++)
    {
        if (cursor_x + i < 128)
            ssd1306_buffer[index + i] = glyph[i];
    }

    // one column space
    if (cursor_x + 5 < 128)
        ssd1306_buffer[index + 5] = 0x00;

    cursor_x += 6;
    if (cursor_x >= 128) cursor_x = 0;
}

void SSD1306_WriteString(const char *str)
{
    while (*str)
    {
        SSD1306_WriteChar(*str++);
    }
}

